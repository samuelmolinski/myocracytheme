<h2>MyOcracy: "My DemOcracy"</h2>
<p>MyOcracy is a tool designed to break down barriers in politics and place your government directly in your hands. In a few clicks of a mouse, you can identify candidates who support your ideas on key issues. In addition to candidates in your own state, you can find candidates for positions in other states and national candidates whose ideas about how to tackle tough issues are similar to yours.</p>
<img src="http://preview.myocracy.org/wp-content/uploads/2013/08/standingout.png" alt="standingout" width="339" height="189" class="alignright size-full wp-image-29" />
<h2>The Challenges of My Democracy in the 21st Century</h2>
<p>In the most literal sense, MyOcracy translates to be “my democracy.” In the theoretical sense, MyOcracy is democracy for the 21st century. We live in an age where our local choices have a global impact, and vice-versa. In this new world, a dining room table decision to purchase a home is but one piece of a chain reaction affecting economic stability in our nation and around the world. In this new world, political instability in one part of the world can lead to a change in summer vacation plans due to sharp increases in gas prices. Never before in history has the world been so dependent on one another. Thus, never before in history have our political choices been so important to determine the life we want to exist now and in the future - for ourselves and for others.</p>
<h2>The Game Changer</h2>
<p>Once you identify candidates who share your ideas, MyOcracy makes it easy for you to support these candidates through donating to their campaigns. From a donation of one dollar to a hundred dollars and beyond, these amounts can increase a candidate’s ability to compete successfully for public office. Modern-day campaigns can be demanding for resources; and, while some of these resources are free to the candidate (e.g., volunteers’ time), the large majority of resources are not (e.g., radio and TV advertisements, street signs, bumper stickers, mailings). A donation made to a candidate’s campaign through MyOcracy can help candidates across the nation raise funds to compete successfully for public office.</p>

<p>This is important because, once elected, these candidates will represent your ideas on government. Therefore, they will need to share your values on important social issues. They will need to seek your input first, as an everyday voter, on issues where powerful special interest groups are present. They will need to speak as your voice and your advocate on tough issues that affect your life and the lives of others. Finally, they will need to understand the supreme power that comes from “the people” when involved in their government.</p>

